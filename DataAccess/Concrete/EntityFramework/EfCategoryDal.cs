        {

            using (NorthwindContext context=new NorthwindContext())//using içerisine yazılan nesneler işi bitince bellekten atılır. Bellekten temizlenir.
            {
                var addedEntity = context.Entry(entity);//referans yakaladık
                addedEntity.State = EntityState.Added; //aslında bu bir eklenecek nesne veritabanında ekleme yaptık.
                context.SaveChanges(); //ekle(üsttteki işlemleri yapar)
            }
        }

        public void Delete(Category entity)
        {
